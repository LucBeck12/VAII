        <p>Za najstaršie sa považuje osídlenie ľudom lužickej kultúry v strednej a mladšej bronzovej dobe z 9. – 7.
            storočia pred Kr. Hmotná kultúra halštatského ľudu oravského typu zo 7. – 4.stor. pred Kr. je v Dolnom
            Kubíne zastúpená nálezmi na ulici Medzihradskej. Dokazujú roľnícko-pastiersku osadu. Z obdobia prelomu
            letopočtu do 4. storočia sa na Medzihradskej ulici našli zlomky rímskej keramiky. Laténska kultúra je
            zastúpená aj na Trninách, Ostrej a Tupej skale. Obydlia tvorili štyri rady chát striedané priechodmi,
            prvotnými ulicami. Obyvatelia sa zaoberali chovom dobytka a pestovaním prosa, hrachu a bôbu. </p>
        <p>V predveľkomoravskom období boli na Trninách a Ostrej skale opevnené hradiská. Tadiaľto viedla významná
            obchodná cesta spájajúca Jadran s Baltom. Dokladom je nález keltských mincí, kupeckých potrieb i tovarov na
            Trninách. V stredoveku obchodnú cestu cez Komjatnú, Dolný Kubín, Tvrdošín, Oravskú Polhoru do Żywieca a
            Wieliczky nazývali soľná, lebo sa využívala na dovoz soli a iných tovarov.</p>
        <p>Prvá písomná zmienka o Kubíne je zo dňa 6. januára 1314 v listine magistra Donča pre liptovských zemanov. V
            roku 1325 magister Donč daroval revúckym zemanom majetok Vyšný Kubín. Začali dejiny rodov Kubínyovcov a
            Meškovcov, ktorých potomkovia žijú na Orave dodnes. Po rozdelení Kubína sa Dolný Kubín vyvíjal ako poddanská
            osada patriaca Oravskému hradu. Bol kráľovským majetkom.</p>
        <p>V 14. storočí boli založené obce, ktoré v súčasnosti tvoria prímestské časti Dolného Kubína: Záskalie, Veľký
            Bysterec, Malý Bysterec, Beňova Lehota, Medzihradné, Srňacie, Mokraď a Kňažia. Veľký Bysterec bol mestečkom
            o 220 rokov skôr ako Dolný Kubín. V ďalšom období nerozvíjal mestské práva a výsady, klesol na úroveň
            ostatných poddanských dedín, lebo v roku 1599 sa spomína ako dedina. Malý Bysterec sa od roku 1355 vyvíjal
            ako zemianska osada. V roku 1380 sa pre tieto obce stal Dolný Kubín duchovným strediskom. Založili tu
            rímskokatolícku farnosť, postavili kostol a zasvätili ho svätej Kataríne Alexandrijskej. Navštevovali ho
            ľudia z dedín od Veličnej po Oravský Podzámok.</p>
        <p>Dolný Kubín sa od počiatku vyvíjal ako remeselnícko-roľnícka osada. Rozvoj remesiel a trhov umožnila
            vynikajúca poloha na obchodnej ceste, ktorá spájala slovenské banské mestá s poľskými trhovými strediskami.
            Najdlhšie živilo Dolnokubínčanov kamenárske remeslo. Na začiatku 14. storočia sa spomína baňa na kameň pre
            výrobu mlecích prostriedkov, žarnovov. Dolnokubínski čižmári, krajčíri, kolári, kožušníci, kováči, korytári,
            mlynári, mäsiari, pekári, kuchári, tesári, stolári a ďalší slúžili hradným pánom a neraz boli odmenení
            povýšením do stavu slobodníkov. </p>
        <p>Reformáciu na Oravu priniesli Turzovci. V roku 1558 sa stali majiteľmi oravského panstva a dolnokubínsky
            kostol sv. Kataríny patril evanjelikom v rokoch 1559 – 1672. Pôsobila tu latinská škola, ktorú navštevovali
            iba chlapci. Palatín Juraj Turzo podporoval rozvoj Dolného Kubína. Zvolával snem župné zhromaždenia oravskej
            šľachty, hoci župným sídlom do roku 1683 bola Veličná. V roku 1604 spustošili Dolný Kubín Bočkajovi hajdúsi,
            ktorí bojovali proti palatínovi Jurajovi Turzovi. Podpálili obec, ostalo len sedem domov, z toho štyri boli
            poškodené. Obyvatelia sa pred hajdúchmi ukryli v lesoch, aby si zachránili život. Po plienení hajdúchov trvá
            veľká bieda.</p>
        <p>V roku 1624 žilo v Dolnom Kubíne 67 rodín, asi 340 obyvateľov. Dňa 14. mája 1632 gróf Gašpar Ilešházi
            (Illesházy) povýšil Dolný Kubín na mestečko za ročný poplatok 400 zlatých a udelil mu právo konať týždenné
            trhy v nedeľu a dva výročné jarmoky na sv. Katarínu a Kvetnú nedeľu. Zároveň Dolnokubínčanom prikázal
            opraviť školu, nemocnicu s útulkom a stavať pekné domy. Dňa 14. decembra 1633 dostal Dolný Kubín mestské
            privilégium od panovníka Ferdinanda II., ktorý povolil ďalšie dva jarmoky. Mestské výsady poskytli Dolnému
            Kubínu hospodársky a kultúrny rast. Ale v nepokojnom 17. storočí, naplnenom náboženskými vojnami a
            povstaniami, sa ocitol neraz na pokraji biedy. Dolný Kubín zažíval obdobie plné reformácie a
            protireformácie. Reformačné hnutie dostalo tvrdý úder potlačením Pikovho povstania v roku 1672 a smrteľný na
            jeseň roku 1683 od litovského vojska poľského kráľa Jána Sobieskeho, ktoré prechádzalo Oravou s cieľom
            pomôcť Habsburgovcom proti Turkom. Litovčania sa k poddaným správali násilnícky, dobyvačne, nechávali za
            sebou spáleniská. Mesto bolo v takej núdzi, že až po piatich rokoch si dalo vyhotoviť nové pečatidlo, lebo
            pôvodné zhorelo v richtárovom dome. Z litovského úpadku sa mesto spamätalo až v 90. rokoch 17. storočia. Dňa
            30. júna gróf Juraj Erdődy vydal Dolnému Kubínu privilégium na vyberanie mýta na dolnokubínskom brode.
            V roku 1712 udelil cisár Karol VI. Habsburský mestu aj jarmočné privilégium. Mesto sa stalo sídlom Oravskej
            župy. Počas Rákociho povstania v roku 1709 zaujali evanjelici kostol sv. Kataríny na dva mesiace. Potom bol
            vrátený katolíkom a evanjelici z mestečka chodili 75 rokov do leštinského artikulárneho kostolíka.
            Tolerančný patent Jozefa II. im vrátil náboženskú slobodu a svoj kostol si postavili v Dolnom Kubíne v roku
            1784.</p>
        <p>V 18. storočí nastalo pokojnejšie mierové obdobie, ale prišli hladomor a morové epidémie. Roky 1715 – 1716
            sú na Orave známe ako zamrznuté roky. Ešte v júni mrzlo a nová zima prišla v septembri, nastal hladomor. V
            roku 1719 mal Dolný Kubín 85 domov a 350 obyvateľov. V priebehu 18. storočia Dolný Kubín výrazne zmenil
            svoju tvár. Dokončil sa mestský dom nazývaný ratúš, v roku 1726 postavili novú nemocnicu, mestský pivovar na
            mieste dnešného evanjelického kostola, v rokoch 1757 – 1758 župný dom, byt župného lekára, lekáreň, kasárne,
            prvý drevený most cez rieku Oravu, tržnicu, mangel, faru, evanjelickú školu. Na ochranu pred požiarmi bolo
            od roku 1730 zakázané používať otvorený oheň v kozuboch. V roku 1776 sa Dolný Kubín stal sídlom slúžnovského
            úradu. Vlastniť grunt patrilo ku kritériám pre udelenie meštianskeho listu a práva voliť richtára a
            mestských prísažných. Zakladanie cechov – obuvníci – 1675, krajčíri a čižmári – 1715. Koncom 18. storočia
            vytvorili združený cech. Cech bol zrušený v roku 1871, kedy vznikla Remeselná spoločnosť a Okresné
            živnostenské spoločenstvo. Obchodom sa zaoberali židia. Hospodárska politika Jozefa II. umožnila rozvoj
            židovského podnikania a náboženskú slobodu. Výstavba domu rabína a kúpeľov.</p>
        <p>Začiatkom 19. storočia sa cez mestečko stavala kráľovská cesta od Kraľovian na Trstenú. V roku 1811 tu
            postavili poštový úrad a v roku 1826 židovskú synagógu. V roku 1813 sa prehnala mestom strašná povodeň,
            začalo sa s budovaním vodnej nádrže na Orave. Veľký rozmach začal v roku 1852, kedy do mestečka prišiel
            prednosta cisársko-kráľovského stoličného úradu František Patočník. Koncom 19. storočia bol Dolný Kubín
            pekným župným mestečkom. V roku 1886 renovovali katolícky kostol sv. Kataríny, ktorý patril medzi osem
            najkrajších pamiatok gotiky na Slovensku. V rokoch 1893 – 1895 postihli námestie dva veľké požiare. Znovu
            postavili župný dom, židovskú synagógu a evanjelický kostol. Vzhľad námestia dotvorila budova štátnych škôl
            (dnešné gymnázium), baroková budova Čaplovičovej knižnice. V 30. rokoch 19. storočia sa Dolný Kubín stal
            strediskom oravských národovcov. Ctiboch Zoch, Juraj Matúška a Leopold Bruck založili v roku 1837 Spoločnosť
            vychovávateľskú v evanjelickom bratrstve oravskom. Členmi boli: Pavol Zoch, Pavol Cancrini, Ján Gregorič,
            Alexander Ruttkay, Václav Jesenský, Ján Zimány, Ľudovít Medzihradský, Daniel Krman, Juraj Šípka, Karol
            Šándor a ďalší. Oravskí národovci sa zišli dňa 24. apríla 1848 v Gäceli a spísali petíciu známu ako Gäceľské
            žiadosti oravské. Petícia bola prirodzeným vyústením národnooslobodzovacieho úsilia. Dobrovoľníci boli
            porazení v Záskalí koncom apríla 1849. V roku 1858 vznikla v Dolnom Kubíne Spoločnosť Čaplovičovej knižnice,
            ktorá sa starala o cenný dar Vavrinca Čaploviča mestu a Orave. Knižnica mala 45 000 zväzkov. Na začiatku 20.
            storočia mal Dolný Kubín 1674 obyvateľov a 211 domov.</p>
        <p>Vznik Česko-slovenskej republiky na jeseň roku 1918 Dolnokubínčania radostne uvítali. Vytvorili miestnu
            národnú radu, v ktorej pôsobil Ignác Radlinský, Dr. Vladimír Pivko, Ladislav Nádaši – Jégé, Aurel Styk,
            Fridrich Medzihradský, Ján Trnkócy, Pavol Samuel Novák, Pavol Országh Hviezdoslav, Dr. Ján Smetanay, Ján
            Diežka, Emil Kňažovič a Július Ballo. Vydávali noviny Naša Orava. </p>
        <p>V roku 1919 vznikla v Dolnom Kubíne účastinárska spoločnosť Oravský priemysel. V tomto roku bol založený
            kultúrno-vzdelávací spolok Hviezdoslavova beseda, ktorej čestným členom bol aj Hviezdoslav. Poriadala
            koncerty, literárne večierky, bábkové divadielka. Zo svojho fondu kupovala knihy, noviny a časopisy,
            podporovala chudobných nadaných študentov a po smrti básnika usilovala o stavbu pomníka nad jeho hrobom. V
            roku 1936 sa zaslúžila o postavenie sochy P. O. Hviezdoslava pred budovou Čaplovičovej knižnice. Bohatú
            kultúrno-osvetovú činnosť vyvíjal v Dolnom Kubíne ženský spolok Živena, ktorý vznikol z podnetu Kornely
            Pivkovej dňa 27. júna 1920. Ďalšie spolky boli: Dobrovoľnícky hasičský spolok (1874), Spolok oravských
            akademikov, Spevokol oravských učiteľov, Ochotnícky, hudobný a spevácky spolok a ďalšie.</p>
        <p>Po skončení II. svetovej vojny sa Dolný Kubín zmenil na významné priemyselné centrum celej Oravy. V rokoch
            1960 – 1996 bol Dolný Kubín správnym centrom celej Oravy. Dolný Kubín je jedinečné mesto v mnohých
            oblastiach. V stavebníctve je to kolonádový most, v kultúre Čaplovičova knižnica, Hviezdoslavovo múzeum,
            Florinov dom, Oravská galéria v bývalom župnom dome, celoslovenská súťaž v prednese poézie a prózy známa ako
            Hviezdoslavov Kubín, medzinárodný hudobný festival Čírenie talentov a festival Bohúňova paleta, v športe
            udeľovanie cien Fair-play MUDr. Ivana Chodáka.</p>
